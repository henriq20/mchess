import { toChessPosition } from '../src/board/position';

it.each([
    [ [0, 0], 'a1' ],
    [ [0, 1], 'b1' ],
    [ [0, 2], 'c1' ],
    [ [0, 3], 'd1' ],
    [ [0, 4], 'e1' ],
    [ [0, 5], 'f1' ],
    [ [0, 6], 'g1' ],
    [ [0, 7], 'h1' ],

    [ [1, 0], 'a2' ],
    [ [2, 0], 'a3' ],
    [ [3, 0], 'a4' ],
    [ [4, 0], 'a5' ],
    [ [5, 0], 'a6' ],
    [ [6, 0], 'a7' ],
    [ [7, 0], 'a8' ],

    [ [1, 1], 'b2' ],
    [ [2, 2], 'c3' ],
    [ [3, 3], 'd4' ],
    [ [4, 4], 'e5' ],
    [ [5, 5], 'f6' ],
    [ [6, 6], 'g7' ],
    [ [7, 7], 'h8' ]
])('should convert %p to %p', ([ x, y ], expected) => {
    expect(toChessPosition(x, y)).toBe(expected);
});
